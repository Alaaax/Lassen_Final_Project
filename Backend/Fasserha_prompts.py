FASSERHA_USER_PROMPT = """\
## النص الشعري
{poem_text}

## نتائج التصنيف
- البحر: {meter_name}
- العصر: {era_name}
- الموضوع: {topic_name}

## مستوى التفسير: {depth}

{depth_instruction}

أرجع JSON فقط — لا تضع أي نص خارج الـ JSON.
"""

DEPTH_BRIEF_INSTRUCTION = """\
اكتب تفسيراً مختصراً — بيت أو بيتان: جملتان أو ثلاث.
لا تطيل. القارئ يريد يفهم بسرعة دون كلام زائد."""

DEPTH_DEEP_INSTRUCTION = """\
اكتب تفسيراً موسّعاً:
- اشرح كل بيت في verses_breakdown
- أضف الصورة البلاغية وأثر البحر
- الأسلوب واضح وقريب من القارئ العام."""


def build_user_prompt(
    poem_text: str,
    meter_name: str,
    era_name: str,
    topic_name: str,
    depth: str = "brief",
) -> str:
    instruction = DEPTH_DEEP_INSTRUCTION if depth == "deep" else DEPTH_BRIEF_INSTRUCTION
    return FASSERHA_USER_PROMPT.format(
        poem_text=poem_text,
        meter_name=meter_name,
        era_name=era_name,
        topic_name=topic_name,
        depth=depth,
        depth_instruction=instruction,
    )